const RTL_LANGS = ['ar'];
const STORAGE_KEY = 'ycbarber_lang';

function applyLanguage(lang) {
    const dict = translations[lang] || translations.fr;

    // Texte simple
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dict[key]) el.textContent = dict[key];
    });

    // Texte avec balises HTML internes (ex: <strong>)
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const key = el.getAttribute('data-i18n-html');
        if (dict[key]) el.innerHTML = dict[key];
    });

    // Attributs aria-label
    document.querySelectorAll('[data-i18n-aria]').forEach(el => {
        const key = el.getAttribute('data-i18n-aria');
        if (dict[key]) el.setAttribute('aria-label', dict[key]);
    });

    // Titre de la page et meta description
    if (dict.page_title) document.title = dict.page_title;
    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc && dict.meta_description) metaDesc.setAttribute('content', dict.meta_description);

    // Direction du texte (RTL pour l'arabe)
    document.documentElement.setAttribute('lang', lang);
    document.documentElement.setAttribute('dir', RTL_LANGS.includes(lang) ? 'rtl' : 'ltr');

    // Mise à jour du bouton de langue
    const activeOption = document.querySelector(`.lang-dropdown li[data-lang="${lang}"]`);
    if (activeOption) {
        document.getElementById('langFlag').textContent = activeOption.getAttribute('data-flag');
        document.getElementById('langCode').textContent = lang.toUpperCase();
        document.querySelectorAll('.lang-dropdown li').forEach(li => li.classList.remove('active'));
        activeOption.classList.add('active');
    }

    localStorage.setItem(STORAGE_KEY, lang);
}

function initLanguageSwitcher() {
    const toggle = document.getElementById('langToggle');
    const dropdown = document.getElementById('langDropdown');
    const switcher = document.getElementById('langSwitcher');

    if (!toggle || !dropdown) return;

    toggle.addEventListener('click', (e) => {
        e.stopPropagation();
        const isOpen = dropdown.classList.toggle('open');
        toggle.setAttribute('aria-expanded', isOpen);
    });

    dropdown.querySelectorAll('li').forEach(item => {
        item.addEventListener('click', () => {
            const lang = item.getAttribute('data-lang');
            applyLanguage(lang);
            dropdown.classList.remove('open');
            toggle.setAttribute('aria-expanded', 'false');
        });
    });

    document.addEventListener('click', (e) => {
        if (switcher && !switcher.contains(e.target)) {
            dropdown.classList.remove('open');
            toggle.setAttribute('aria-expanded', 'false');
        }
    });

    // Langue enregistrée, sinon français par défaut
    const savedLang = localStorage.getItem(STORAGE_KEY);
    applyLanguage(savedLang && translations[savedLang] ? savedLang : 'fr');
}

document.addEventListener('DOMContentLoaded', initLanguageSwitcher);
